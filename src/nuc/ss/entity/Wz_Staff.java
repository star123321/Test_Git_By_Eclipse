package nuc.ss.entity;

public class Wz_Staff {

	private String wzStaffNo;
	private String wzStaffName;
	private String wzUnit;
	private String wzStaffPhone;
	private String wzStaffQQ;
	private String wzTime;
	
	public Wz_Staff() {}

	public Wz_Staff(String wzStaffNo, String wzStaffName, String wzUnit, String wzStaffPhone, String wzStaffQQ,
			String wzTime) {
		this.wzStaffNo = wzStaffNo;
		this.wzStaffName = wzStaffName;
		this.wzUnit = wzUnit;
		this.wzStaffPhone = wzStaffPhone;
		this.wzStaffQQ = wzStaffQQ;
		this.wzTime = wzTime;
	}

	public String getWzStaffNo() {
		return wzStaffNo;
	}

	public void setWzStaffNo(String wzStaffNo) {
		this.wzStaffNo = wzStaffNo;
	}

	public String getWzStaffName() {
		return wzStaffName;
	}

	public void setWzStaffName(String wzStaffName) {
		this.wzStaffName = wzStaffName;
	}

	public String getWzUnit() {
		return wzUnit;
	}

	public void setWzUnit(String wzUnit) {
		this.wzUnit = wzUnit;
	}

	public String getWzStaffPhone() {
		return wzStaffPhone;
	}

	public void setWzStaffPhone(String wzStaffPhone) {
		this.wzStaffPhone = wzStaffPhone;
	}

	public String getWzStaffQQ() {
		return wzStaffQQ;
	}

	public void setWzStaffQQ(String wzStaffQQ) {
		this.wzStaffQQ = wzStaffQQ;
	}

	public String getWzTime() {
		return wzTime;
	}

	public void setWzTime(String wzTime) {
		this.wzTime = wzTime;
	}
	
}
